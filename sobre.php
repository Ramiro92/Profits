  </p>
  <p class="text-justify"> 
Sin embargo, Alfredo Martínez, tenía la convicción de que se podía lograr la libertad financiera haciendo negocio online y, buscando oportunidades, se encontró con el mercado bursátil. El primer contacto lo tuvo con un bróker americano, hizo una pequeña inversión de $200.00 pero debido al nulo conocimiento sobre el tema dicha inversión iba en pérdida por lo que decidió retirar los fondos que aún quedaban. Luego tuvo contacto con una persona que conocía más sobre el tema, aprendió bastante pero no lo suficiente como para ser rentable, esto pudo comprobarlo cuando, apoyado por una socia, hizo una inversión de $2,000.00 de lo cual sólo recuperó $800.00.
</p>
<p class="text-justify"> 
Con este historial de pérdidas decidió aprender de los profesionales y contactó con un español, especialista en el tema, que tenía ya muchos años de experiencia y vivía del trading, tomó sus cursos gratuitos y pagados. Además, compró otros cursos a precios muy altos. 
</p>
<p class="text-justify">El haber recibido formación profesional, sobre trading en divisas, le permitió lograr la rentabilidad de modo eficiente y constante. </p>
